    public void loadData() {
        StudentsImpl s = new StudentsImpl();
        list = s.loadData();
        for (Students list1 : list) {
            datatbl.addRow(list1.toStringArr());
        }

    }

    public void checkId() {
        for (Students list1 : list) {
            p = p.compile("[0-9]+");
            Matcher m = p.matcher(txtId.getText());           
            if (m.matches()) {
                if (txtId.getText().equals(String.valueOf(list1.getId()))) {
                    lblcheckID.setText("id is already existed");
                } else {
                    lblcheckID.setText("OK");
                    isIdValid=true;
                };
            }else{
            lblcheckID.setText("pls Enter Number!");
            }
        }
        CheckAll();
    }
    public void CheckAll(){
    if(file!=null&&isIdValid==true){
        btnAdd.setEnabled(true);
    }
    }
    

