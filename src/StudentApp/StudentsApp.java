    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        StudentsImpl s = new StudentsImpl();
        int get = s.addData(Integer.parseInt(txtId.getText()), txtName.getText(), Integer.parseInt(txtPhone.getText()), txtClass.getText(), file);
        if (get == 0) {
            JOptionPane.showMessageDialog(rootPane, "Error!");
        }
        loadData();
        isIdValid=false;
        file=null;
        btnAdd.setEnabled(false);
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnChooseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChooseActionPerformed
        JFileChooser chooser = new JFileChooser();
        FileNameExtensionFilter filter = new FileNameExtensionFilter("Images", "jpg");
        chooser.setFileFilter(filter);
        int get = chooser.showDialog(null, "Run Application");
        if (get == JFileChooser.APPROVE_OPTION) {
            file = chooser.getSelectedFile();
            lblFile.setText(file.getPath());
            BufferedImage img = null;
            try {
                img = ImageIO.read(file);
            } catch (IOException ex) {
                Logger.getLogger(StudentsApp.class.getName()).log(Level.SEVERE, null, ex);
            }
            Image bi = img.getScaledInstance(300, 175, Image.SCALE_DEFAULT);
            ImageIcon icon = new ImageIcon(bi);
            lblImg.setIcon(icon);
        }
        CheckAll();
    }//GEN-LAST:event_btnChooseActionPerformed

    private void tblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMouseClicked
        BufferedImage img = null;
        try {
            img = ImageIO.read(list.get(tbl.getSelectedRow()).getF());
        } catch (IOException ex) {
            Logger.getLogger(StudentsApp.class.getName()).log(Level.SEVERE, null, ex);
        }
        Image after = img.getScaledInstance(500, 375, Image.SCALE_SMOOTH);
        ImageIcon icon = new ImageIcon(after);
        lblImgShow.setIcon(icon);
    }//GEN-LAST:event_tblMouseClicked

